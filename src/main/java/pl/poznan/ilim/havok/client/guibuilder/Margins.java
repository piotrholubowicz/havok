package pl.poznan.ilim.havok.client.guibuilder;

/**
 * Utility class providing margin values used throughout application.
 * @author MKorecki
 */
public class Margins {
	/**
	 * Narrow margin for simple components (controls, widgets) determining
	 * distance form each other within container.
	 */
	public static final int COMPONENTS_NARROW = 5;
	
	/**
	 * Wide margin for simple components (controls, widgets) determining
	 * distance from each other within container.
	 */
	public static final int COMPONENTS_WIDE = 10;
	
	/**
	 * Narrow margin for container controls (layouts) determining space between
	 * container edges and its members.
	 */
	public static final int CONTAINER_NARROW = 10;
	
	/**
	 * Wide margin for container controls (layouts) determining space between
	 * container edges and its members.
	 */
	public static final int CONTAINER_WIDE = 10;
}
